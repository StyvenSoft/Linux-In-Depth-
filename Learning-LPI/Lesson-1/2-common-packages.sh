#!/bin/bash

# Otros paquetes comunes en Debian

# htop: Una herramienta de monitoreo del sistema en la terminal, similar a top, pero con una interfaz más amigable.

sudo apt install htop

# curl: Herramienta para transferir datos desde o hacia un servidor utilizando protocolos como HTTP, FTP, etc.

sudo apt install curl

# 
# vim o nano: Editores de texto en la terminal. nano es más sencillo, mientras que vim es más poderoso y ampliamente utilizado.

sudo apt install vim

sudo apt install nano

# git: Sistema de control de versiones distribuido.

sudo apt install git

# build-essential: Un paquete que instala un conjunto de herramientas para compilar software en Debian, 
# incluyendo el compilador gcc, g++, y make.

sudo apt install build-essential

# wget: Herramienta de descarga de archivos desde la web en la terminal.

sudo apt install wget

# gnome-tweaks (si usas el entorno de escritorio GNOME): Herramienta para ajustar configuraciones avanzadas del escritorio GNOME.

sudo apt install gnome-tweaks

# net-tools: Paquete que incluye utilidades de red como ifconfig, netstat, etc. En las versiones más 
# recientes de Debian, se prefiere ip del paquete iproute2, pero aún puede ser útil.

sudo apt install net-tools

# ufw (Uncomplicated Firewall): Una interfaz simple para manejar las reglas de iptables, ideal para gestionar firewalls.

sudo apt install ufw

# gnupg: Paquete para cifrado y firmas digitales usando el protocolo OpenPGP.

sudo apt install gnupg





